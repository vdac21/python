weekday=int(input("enter weekday day number(1-7): "))

if weekday==1:
    print("monday")
elif weekday==2:
    print("tuesday")
elif weekday==3:
    print("wednesday")
elif weekday==4:
    print("thursday")
elif weekday==5:
    print("friday")
elif weekday==6:
    print("saturday")
elif weekday==7:
    print("sunday")
else:
    print("enter weekday number between 1-7.")
